"use strict"

// BUCLE WHILE

// Es muy parecido al cilo FOR  solo que este es menos extricto de forma que se puede hacer bucles infinitios 
// y es un bucle indefinido que consta de una condicion  y cuando se cumpla la condicion se ejecuta un grupo
// de instrucciones infinitamente. 
// si se le pone un contador el bucle llega al momento que sale del bucle 
    

// ejemplo incremeto 

var year = 2018;

while (year <= 2079){
    //ejecuta esto
    console.log("estamos en el año:"+ year )

    year++;
}

// ------------------------------------------------------------------------------

// ejemplo decremento

while (year != 1989){
    //ejecuta esto
    console.log("estamos en el año:"+ year )

    year--;
}



