"use strict"

// BUCLE FOR
// se puede conocer como : bucles, ciclos, iteradores etc.
// un bucle es una estructura de cintrol que se repite varias veses.
// esta formado por el: contador, condicion y por el incrementador 

var numero = 1000;
// ++ es el operador de incremento
for (var i = 0; i <= numero; i++) {
    console.log("numero: " + i);
    debugger;
}






